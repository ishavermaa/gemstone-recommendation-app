import mongoose from 'mongoose';

const reviewSchema = new mongoose.Schema(
  {
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    gemstone: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'Gemstone',
      required: true,
    },
    rating: {
      type: Number,
      min: 1,
      max: 5,
      required: true,
    },
    comment: {
      type: String,
      maxlength: 500,
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
    updatedAt: {
      type: Date,
      default: Date.now,
    },
  },
  { timestamps: true }
);

// Index for unique review per user per gemstone
reviewSchema.index({ user: 1, gemstone: 1 }, { unique: true });

export default mongoose.model('Review', reviewSchema);
